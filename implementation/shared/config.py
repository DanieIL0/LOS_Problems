def parse_trial_dir_name(trial_dir):
    parts = trial_dir.split('_')
    trial_date = parts[0]
    trial_number = ''
    trial_type = ''
    if len(parts) >= 2:
        trial_type = parts[1]
        if len(parts) == 3:
            trial_number = parts[2]
        elif len(parts) > 3:
            trial_type = '_'.join(parts[1:-1])
            trial_number = parts[-1]
    return trial_date, trial_type, trial_number

